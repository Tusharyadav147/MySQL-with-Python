with CTE as(
select b.name as Department, a.Name as Employee, a.Salary, 
dense_rank() over(partition by b.id order by salary desc) as ranks 
from employee a 
join 
department b 
on b.Id = a.DepartmentId)
select department, Employee, Salary from CTE where ranks <= 3;